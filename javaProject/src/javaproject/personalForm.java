    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Component frame = null;
        int choice = JOptionPane.showOptionDialog(frame, "Select a field", "Clear Options",
                JOptionPane.YES_NO_CANCEL_OPTION, 
                JOptionPane.QUESTION_MESSAGE, 
                null, 
                new String[]{"Clear All", "Clear Section", "Cancel"},
                "Clear" + "");
        if(choice == 0){
            txtName.setText(""); LblName.setText(""); txtLastname.setText(""); LblLastname.setText(""); 
            txtEmail.setText(""); LblEmail.setText(""); txtPhone.setText(""); LblPhone.setText(""); txtID.setText("");
            LblIDD.setText(""); LblCrime.setText(""); LblGender.setText(""); 
            txtAddress.setText(""); LblAddress.setText(""); txtPassword.setText(""); LblPassword.setText(""); 
            txtAge.setText(""); LblAgee.setText(""); txtDisability.setText(""); LblDisability.setText("");
        }
        else if(choice == 1){
            String section = JOptionPane.showInputDialog("Enter field to clear");
            if(section.equalsIgnoreCase("name")){ 
                txtName.setText(""); LblName.setText("");
            }
            else if(section.equalsIgnoreCase("lastname")){
                txtLastname.setText(""); LblLastname.setText(""); 
            }
            else if(section.equalsIgnoreCase("email")){
                txtEmail.setText(""); LblEmail.setText("");
            }
            else if(section.equalsIgnoreCase("ID")){
                txtID.setText(""); LblIDD.setText("");
            }
            else if(section.equalsIgnoreCase("physical address")){
                txtAddress.setText(""); LblAddress.setText("");
            }
            else if(section.equalsIgnoreCase("password")){
                txtPassword.setText(""); LblPassword.setText("");
            }
            else if(section.equalsIgnoreCase("age")){
                txtAge.setText(""); LblAgee.setText("");
            }
            else if(section.equalsIgnoreCase("disabilities")){
                txtDisability.setText(""); LblDisability.setText("");
            }
            else if(section.equalsIgnoreCase("phone")){
                txtPhone.setText(""); LblPhone.setText("");
            }
            else{
                JOptionPane.showMessageDialog(frame, "invalid");
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

