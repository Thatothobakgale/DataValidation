    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        String NAMES = txtName.getText(), LASTNAME = txtLastname.getText(), EMAIL = txtEmail.getText(), ID = txtID.getText();
        String PASSWORD = txtPassword.getText();
        String AGE = txtAge.getText(), PHONE = txtPhone.getText();
        String ADDRESS = txtAddress.getText(), DISABILITIES = txtDisability.getText();  
        
        if(txtName.getText().isEmpty() && txtLastname.getText().isEmpty() && txtLastname.getText().isEmpty() && txtEmail.getText().isEmpty() 
                && txtPhone.getText().isEmpty() && txtID.getText().isEmpty() && txtAddress.getText().isEmpty()
                && txtDisability.getText().isEmpty() && txtPassword.getText().isEmpty() && txtAge.getText().isEmpty()){
            
            LblName.setText("Invalid"); LblName.setForeground(Color.red); LblLastname.setText("Invalid"); LblLastname.setForeground(Color.red);
            LblEmail.setText("Invalid"); LblEmail.setForeground(Color.red); LblDisability.setText("Invalid"); LblDisability.setForeground(Color.red);
            LblPhone.setText("Invalid"); LblPhone.setForeground(Color.red); LblIDD.setText("Invalid"); LblIDD.setForeground(Color.red);
            LblAddress.setText("Invalid"); LblAddress.setForeground(Color.red); LblPassword.setText("Invalid"); LblPassword.setForeground(Color.red);
            LblAgee.setText("Invalid"); LblAgee.setForeground(Color.red); LblGender.setText("Invalid"); LblGender.setForeground(Color.red);  
            LblCrime.setText("Invalid"); LblCrime.setForeground(Color.red);
        }
       else if(!NAMES.matches("[a-zA-Z]+") || NAMES.length() < 3){
            LblName.setText("Atleast 3 Characters with no letters");
            LblName.setForeground(Color.yellow);
        }
        else if(LASTNAME.length() < 3 || !LASTNAME.matches("[a-zA-Z]+")){
            LblLastname.setText("Atleast 3 Characters");
            LblLastname.setForeground(Color.yellow);
        }
        else if(!EMAIL.contains("@gmail.com")){
            LblEmail.setText("Invalid email");
            LblEmail.setForeground(Color.yellow);
        }
        else if(ID.length() < 13 || ID.length() > 13){
            LblIDD.setText("13 digits/characters");
            LblIDD.setForeground(Color.yellow);
        }
        else if(!AGE.matches("[0-9]+") || AGE.length() < 2 || AGE.length() > 2){
            LblAgee.setText("2 integers only");
            LblAgee.setForeground(Color.yellow);
        }
        else if(PHONE.length() < 10 || PHONE.length() >10 || !PHONE.matches("[0-9]+") || !PHONE.startsWith("0")){
            LblPhone.setText("Must start with a 0 and contain 10 integers only");
            LblPhone.setForeground(Color.yellow);
        }
        else if(!ADDRESS.contains("street") && !ADDRESS.contains("[a-zA-Z0-9]+")){
             LblAddress.setText("Must contain street and number");
             LblAddress.setForeground(Color.yellow);
        }
        else if(!DISABILITIES.equalsIgnoreCase("yes") && !DISABILITIES.equalsIgnoreCase("no")){
            LblDisability.setText("Enter yes or no, if yes please specify your disability");
            LblDisability.setForeground(Color.yellow); 
        }
        else if(!RadFemale.isSelected() && !RadMale.isSelected() && !RadLGBT.isSelected() && !RadYes.isSelected() 
                && !RadNo.isSelected()){
            LblGender.setText("Select one button");
            LblGender.setForeground(Color.yellow);
            LblCrime.setText("Select one button");
            LblCrime.setForeground(Color.yellow);
        }
        else if(RadYes.isSelected()){
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "You can't carry on with the application because you have a criminal record");
        }
        else if(PASSWORD.length() < 8){
            LblPassword.setText("Must be 8 characters long, with one uppercase,& one digit");
            LblPassword.setForeground(Color.yellow);
        }
        else {
            LblDisability.setText("Valid"); LblDisability.setForeground(Color.GREEN); LblIDD.setText("Valid"); LblIDD.setForeground(Color.GREEN); 
            LblLastname.setText("Valid"); LblLastname.setForeground(Color.GREEN); LblPhone.setText("Valid"); 
            LblPhone.setForeground(Color.GREEN); LblAddress.setText("Valid"); LblAddress.setForeground(Color.GREEN); 
            LblName.setText("Valid"); LblName.setForeground(Color.GREEN); LblAgee.setText("Valid"); 
            LblAgee.setForeground(Color.GREEN); LblEmail.setText("Valid"); LblEmail.setForeground(Color.GREEN);
            LblGender.setText("Valid"); LblGender.setForeground(Color.GREEN); LblCrime.setText("Valid"); 
            LblCrime.setForeground(Color.GREEN); LblPassword.setText("Valid"); LblPassword.setForeground(Color.GREEN);
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Confirm that you are not a robot");
        }
    }//GEN-LAST:event_BtnSaveActionPerformed

